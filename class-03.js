// STRING CONCATENATION //
let firstname = 'ZAIN';
let secondname = 'BABA';
let fullname = firstname + ' ' + secondname;
console.log(fullname);
// TEMPLETE LITERALS //
let firstName = 'Zain';
let secondName = 'Baba';
let fullName = `${firstName} ${secondName}`;
console.log(fullName);
// OPERATORS IN TYPESCRIPT //
// ADDITION { + }
let num1 = 2;
let num2 = 4;
let add = num1 + num2;
console.log(add); // 6
// SUBTRACTION { - }
let num3 = 6;
let num4 = 4;
let sub = num3 - num4;
console.log(sub); // 2
// MULTIPLICATION{ * }
let num5 = 6;
let num6 = 4;
let multiply = num5 * num6;
console.log(multiply); // 24
// DIVISION { / }
let num7 = 6;
let num8 = 12;
let divide = num7 / num8;
console.log(divide); // 0.5
// EXPONENTIATION { ** }
let num9 = 6;
let num10 = 4;
let power = num9 ** num10;
console.log(power); // 1296
// MODULUS { % }
let num11 = 11;
let num12 = 3;
let modulus = num11 % num12;
console.log(modulus); // 2
