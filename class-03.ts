// STRING CONCATENATION //
let firstname: string = 'ZAIN';
let secondname: string = 'BABA';
let fullname: string = firstname+' '+secondname;
console.log(fullname);

// TEMPLETE LITERALS //
let firstName: string = 'Zain';
let secondName: string = 'Baba';
let fullName: string = `${firstName} ${secondName}`;
console.log(fullName);

// OPERATORS IN TYPESCRIPT //

// ADDITION { + }
let num1:number = 2;
let num2:number = 4;
let add:number = num1+num2;
console.log(add); // 6

// SUBTRACTION { - }
let num3:number = 6;
let num4:number = 4;
let sub:number = num3-num4;
console.log(sub); // 2

// MULTIPLICATION{ * }
let num5:number = 6;
let num6:number = 4;
let multiply:number = num5*num6;
console.log(multiply); // 24

// DIVISION { / }
let num7:number = 6;
let num8:number = 12;
let divide:number = num7/num8;
console.log(divide); // 0.5

// EXPONENTIATION { ** }
let num9:number = 6;
let num10:number = 4;
let power:number = num9 ** num10;
console.log(power); // 1296

// MODULUS { % }
let num11:number = 11;
let num12:number = 3;
let modulus:number = num11%num12;
console.log(modulus); // 2
